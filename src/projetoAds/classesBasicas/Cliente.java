public class Cliente {

    private String cli_cpf;
    private String cli_nome;
    private Integer cli_id;

    /**
     * @return the cli_cpf
     */
    public String getCli_cpf() {
        return cli_cpf;
    }

    /**
     * @param cli_cpf the cli_cpf to set
     */
    public void setCli_cpf(String cli_cpf) {
        this.cli_cpf = cli_cpf;
    }

    /**
     * @return the cli_nome
     */
    public String getCli_nome() {
        return cli_nome;
    }

    /**
     * @param cli_nome the cli_nome to set
     */
    public void setCli_nome(String cli_nome) {
        this.cli_nome = cli_nome;
    }

    /**
     * @return the cli_id
     */
    public Integer getCli_id() {
        return cli_id;
    }

    /**
     * @param cli_id the cli_id to set
     */
    public void setCli_id(Integer cli_id) {
        this.cli_id = cli_id;
    }
    
    
}
